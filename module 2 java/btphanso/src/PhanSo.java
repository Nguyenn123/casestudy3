public class PhanSo {
    private double tuso;
    private double mauso;

    public PhanSo() {
    }

    public PhanSo(double tuso, double mauso) {
        this.tuso = tuso;
        this.mauso = mauso;
    }


    public double getTuso() {
        return tuso;
    }

    public void setTuso(double tuso) {
        this.tuso = tuso;
    }

    public double getMauso() {
        return mauso;
    }

    public void setMauso(double mauso) {
        this.mauso = mauso;
    }
}
